var ids = [];
export function createId(){
    var id = undefined;
    while(id == undefined){
        id = tryCreateId();
    }
    ids.push(id);
    return id;
}

function tryCreateId(){
    const temp = adjectives[Math.floor(Math.random() * adjectives.length)]
    + names[Math.floor(Math.random() * names.length)];
    for(let i = 0; i < ids.length; i++){
        if(ids[i] == temp) return undefined;
    }
    return temp;
}